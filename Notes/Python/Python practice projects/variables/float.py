# Formatting with Tokens

# Example 4: Format strings

# Octal format (%o)
print ("18 (decimal) as an octal value: %o" % 18)
# Output: 18 (decimal) as an octal value: 22

# Uppercase Hexadecimal format (%X)
print ("45 (decimal) as an uppercase (X) hexadecimal value: %X"% 45)
# Output: 45 (decimal) as an uppercase (X) hexadecimal value: 2D

# Exponential format (%e)
print("4.6 (float): (e) floating point exponential value: %e"% 4.6)
# Output: 4.6 (float): (e) floating point exponential value: 4.600000e+00
