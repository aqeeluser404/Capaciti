# Complex Numbers

# Example 5: Complex numbers

# Define complex numbers
a_comp = complex(4, 8)  # a_comp is a complex number with real part 4 and imaginary part 8
i1 = 5
i2 = 6
b_comp = complex(i1, i2)  # b_comp is a complex number with real part 5 and imaginary part 6

# Print real and imaginary parts of a_comp
print(a_comp.real)  # Output: 4.0
print(a_comp.imag)  # Output: 8.0

# Calculate and print the absolute value of a_comp
print(abs(a_comp))  # Output: 8.94427191

# Add complex numbers
print(a_comp + 5 + 6j)  # Output: (9+14j)
print(7 + 5j + 10 + 4j)  # Output: (17+9j)